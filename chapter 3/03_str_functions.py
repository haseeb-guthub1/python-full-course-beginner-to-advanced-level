name = "Haseeb"
print(len(name))  # prints the length of the string, which is 6

print(name.endswith("eeb"))  # prints True because the string ends with "eeb"
print(name.startswith("Has"))  # prints True because the string starts with "Has"
print(name.capitalize())  # prints "Haseeb" because it capitalizes the first letter of the string
print(name.upper())  # prints "HASEEB" because it converts the string to uppercase
print(name.lower())  # prints "haseeb" because it converts the string to lowercase
print(name.title())  # prints "Haseeb" because it capitalizes the first letter of each word in the string
print(name.find("b"))  # prints 5 because the first occurrence of "b" is at index 5
