name = "Haseeb"

print(name[-4:-1])  # prints "see" because it starts from the fourth character from the end and goes up to but not including the last character


print(name[1:4])  # prints "ase" because it starts from the second character and goes up to but not including the fourth character


print(name[0:5: 2])  # prints "Hse" because it starts from the first character and goes up to but not including the fifth character, taking every second character